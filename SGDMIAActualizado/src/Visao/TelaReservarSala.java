    /**
     * butao responsavel por reservar um determinado material.
     * @param evt 
     */
    private void jbtReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtReservarActionPerformed
        // TODO add your handling code here:
        ReservarSala b = new ReservarSala();
        ReservarSalaDAO estu = new ReservarSalaDAO();
        
        int contador=0;
        for(ReservarSala reservar: estu.consultar() ){
              if(reservar.getSalas().equals(jcbSalas.getSelectedItem()))
               contador++;
              
        }

        if(contador==0){
        Docente docente = (Docente) jcbNomeDocentes.getSelectedItem();
        b.setDocentes(docente);
        Funcionario funcionario = (Funcionario) jcbNomeFuncionarios.getSelectedItem();
        b.setNomefuncionario(funcionario);
        Salas salas = (Salas) jcbSalas.getSelectedItem(); 
        b.setSalas(salas);
        b.setEstado(Constantes.ESTADO_REGISTO_RESERVADO);
        b.setData(new Date());

        estu.gravar(b);
            JOptionPane.showMessageDialog(null, "A sala foi reservda com sucesso");
            jcbNomeDocentes.setSelectedIndex(0);
            jcbNomeFuncionarios.setSelectedIndex(0);
            jcbSalas.setSelectedIndex(0);
            readJTable();
        } else {
            JOptionPane.showMessageDialog(null, "Nao foi possivel reservar a sala!!!.");
        }

     
    }//GEN-LAST:event_jbtReservarActionPerformed

    private void jbtActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtActualizarActionPerformed
        // TODO add your handling code here:
        if (tabela.getSelectedRow() != -1) {
            ReservarSala b = new ReservarSala();
            ReservarSalaDAO estu = new ReservarSalaDAO();
            
            b.setDocentes((Docente) jcbNomeDocentes.getSelectedItem());
            b.setNomefuncionario((Funcionario) jcbNomeFuncionarios.getSelectedItem());
            b.setSalas((Salas) jcbSalas.getSelectedItem());
            b.setData((Date) tabela.getValueAt(tabela.getSelectedRow(), 3));
            b.setEstado(Constantes.ESTADO_REGISTO_RESERVADO);

            if (estu.atualizar(b)) {
                JOptionPane.showMessageDialog(null, "Actualizacao efectuada com sucesso");
                jcbNomeFuncionarios.setSelectedIndex(0);
                jcbNomeFuncionarios.setSelectedIndex(0);
                jcbSalas.setSelectedIndex(0);
                readJTable();
                setStep(STEP_CRIAR);

            } else {
                JOptionPane.showMessageDialog(null, "Nao foi possivel actualizar a reserva!!!");
            }

        }
    }//GEN-LAST:event_jbtActualizarActionPerformed

