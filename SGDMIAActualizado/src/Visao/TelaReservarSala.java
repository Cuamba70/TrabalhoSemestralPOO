    /**
     * metodo que adciona todos funcionario registadas na combo box nomefuncionarios 
     * para a requisicao de um material
     */
    public void adcionarFuncionario() {
        FuncionarioDAO dao = new FuncionarioDAO();
        for (Funcionario t : dao.consular()) {
            if ((t.getEstado().equalsIgnoreCase(Constantes.ESTADO_REGISTO_VIGOR))) {
                jcbNomeFuncionarios.addItem(t);
            }
        }
    }

    /**
     * metodo que adciona todos estudantes registadas na combo box nomeestudantes 
     * para a requisicao de um material
     */
    public void adcionarDocente() {
        DocenteDAO dao = new DocenteDAO();
        for (Docente t : dao.consular()) {
          if ((t.getEstado().equalsIgnoreCase(Constantes.ESTADO_REGISTO_VIGOR))) {
            jcbNomeDocentes.addItem(t);
          }
        }
    }
    public void adcionarSala() {
        SalasDAO dao = new SalasDAO();
        for (Salas t : dao.consular()) {
          if ((t.getEstado().equalsIgnoreCase(Constantes.ESTADO_REGISTO_VIGOR))&&(t.getCategoria().equalsIgnoreCase("Simples")||t.getCategoria().equalsIgnoreCase("Laboratorio"))) {
            jcbSalas.addItem(t);
          }
        }
    }

    /**
     * Metodo responsavel por buscar os dados na base de dados e mostrar na tabela.
     */
    public void readJTable() {
        DefaultTableModel modelo1 = (DefaultTableModel) tabela.getModel();
        modelo1.setNumRows(0);
        ReservarSalaDAO dau = new ReservarSalaDAO();

        for (ReservarSala a : dau.consultar()) {
            if ((a.getEstado().equalsIgnoreCase(Constantes.ESTADO_REGISTO_RESERVADO))) {
                modelo1.addRow(new Object[]{ a.getDocentes(),
                    a.getNomefuncionario(), a.getSalas(), a.getData(),a.getCod_reserva()

                });
            }
        }
    }

    /**
     * Metodo responsavel buscar os dados pesquisados e mostrar na tabela.
     */
    public void readJTable1() {
        DefaultTableModel modelo1 = (DefaultTableModel) tabela.getModel();
        modelo1.setNumRows(0);
        ReservarSalaDAO dau = new ReservarSalaDAO();

        for (ReservarSala a : dau.consultar()) {
            if (a.getDocentes().equals(jcbNomeDocentes.getSelectedItem())||a.getSalas().equals((Salas) jcbSalas.getSelectedItem())) {

                modelo1.addRow(new Object[]{a.getDocentes(),
                    a.getNomefuncionario(), a.getSalas(), a.getData()

                });
            }
        }
    }

    /**
     * metodo que ajuda a desabilitar alguns campos e butoes na tela.
     * @param step 
     */
    public void setStep(String step) {

        if (step.equalsIgnoreCase(STEP_CRIAR)) {
            jbtReservar.setVisible(true);
            jbrPesquisar.setVisible(true);
            jbtLimpar.setVisible(true);
            jbtActualizar.setVisible(false);
            jbtEditar.setVisible(false);
            jbtVoltar.setVisible(false);
            jcbNomeDocentes.setEnabled(true);
            jcbNomeFuncionarios.setEnabled(true);
            jcbSalas.setEnabled(true);

        } else if (step.equalsIgnoreCase(STEP_VISUALIZAR)) {
            jbtReservar.setVisible(false);
           
            jbrPesquisar.setVisible(false);
            jbtLimpar.setVisible(false);
            jbtActualizar.setVisible(false);
            jbtEditar.setVisible(true);
            jbtVoltar.setVisible(true);
            jcbNomeDocentes.setEnabled(false);
            jcbNomeFuncionarios.setEnabled(false);
            jcbSalas.setEnabled(false);
           

        } else if (step.equalsIgnoreCase(STEP_EDITAR)) {
            jbtReservar.setVisible(false);
            jbrPesquisar.setVisible(false);
            jbtLimpar.setVisible(true);
            jbtActualizar.setVisible(true);
            jbtEditar.setVisible(false);
            jbtVoltar.setVisible(true);
            jcbNomeDocentes.setEnabled(true);
            jcbNomeFuncionarios.setEnabled(true);
            jcbSalas.setEnabled(true);
        }
    }

    

