    /**
     * butao responsavel por gravar os dados na base dados.
     * @param evt 
     */
    private void jbtGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtGravarActionPerformed
        // TODO add your handling code here:
        Material b = new Material();

        MarerialDAO estu = new MarerialDAO();

        int contador = 0;
        for (Material a : estu.consular()) {
            if (a.getCodigo().equalsIgnoreCase(jtfCodigo.getText())) {
                contador++;
            }
        }
        if (contador == 0) {

            b.setNome((String) jcbNome.getSelectedItem());
            b.setCodigo(jtfCodigo.getText().toUpperCase());
            b.setQuantidade(1);
            b.setEstado(Constantes.ESTADO_REGISTO_VIGOR);
            b.setData(new Date());
            estu.gravar(b);
            JOptionPane.showMessageDialog(null, "O material foi registado com sucesso");
            jcbNome.setSelectedIndex(0);
            jtfCodigo.setText(null);

            readJTable();
        } else {
            JOptionPane.showMessageDialog(null, "O Codigo introduzido ja foi registado.");
        }

    
    }//GEN-LAST:event_jbtGravarActionPerformed

