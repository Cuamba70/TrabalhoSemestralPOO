    /**
     * metodo que adciona todas salas dos estudantes registadas na combo box sala 
     * para o registo de um estudante.
     */
    public void adcionarSalaEsudante() {
        SalasDAO dao = new SalasDAO();
        for (Salas t : dao.consular()) {
            if ((t.getEstado().equalsIgnoreCase(Constantes.ESTADO_REGISTO_VIGOR))&&((t.getCategoria().equalsIgnoreCase("Simples")))) {
                jcbSala.addItem(t);
            }
        }
    }
    
    /**
     * Metodo responsavel por buscar os dados na base de dados e mostrar na tabela.
     */
    public void readJTable() {
        DefaultTableModel modelo1 = (DefaultTableModel) tabela.getModel();
        modelo1.setNumRows(0);
        EstudanteDAO dau = new EstudanteDAO();

        for (Estudante a : dau.consular()) {
            if ((a.getEstado().equalsIgnoreCase(Constantes.ESTADO_REGISTO_VIGOR))) {
                modelo1.addRow(new Object[]{a.getCodigo(), a.getNome(), a.getCurso(),
                    a.getSala(), a.getRegime(), a.getData()

                });
            }
        }
    }

    /**
     * Metodo responsavel buscar os dados pesquisados e mostrar na tabela.
     */
    public void readJTable1() {
        DefaultTableModel modelo1 = (DefaultTableModel) tabela.getModel();
        modelo1.setNumRows(0);
        EstudanteDAO dau = new EstudanteDAO();

        for (Estudante a : dau.consular()) {
   
          if  (a.getNome().toLowerCase().contains(jtfNome.getText().toLowerCase())||a.getCurso().equalsIgnoreCase((String) jcbCurso.getSelectedItem())) {
                modelo1.addRow(new Object[]{a.getCodigo(), a.getNome(),
                    a.getCurso(), a.getSala(), a.getRegime(), a.getData()

                });
            }
        }
    }

    /**
     * metodo que ajuda a desabilitar alguns campos e butoes na tela.
     * @param step 
     */
    public void setStep(String step) {

        if (step.equalsIgnoreCase(STEP_CRIAR)) {
            jbtGravar.setVisible(true);
            jbrPesquisar.setVisible(true);
            jbtLimpar.setVisible(true);
            jbtActualizar.setVisible(false);
            jbtEditar.setVisible(false);
            jbtRemover.setVisible(false);
            jbtVoltar.setVisible(false);
            jtfNome.setEnabled(true);
            jtfCodigo.setEnabled(true);
            jcbSala.setEnabled(true);
            jcbCurso.setEnabled(true);
            jcbRegime.setEnabled(true);

        } else if (step.equalsIgnoreCase(STEP_VISUALIZAR)) {
            jbtGravar.setVisible(false);
            jbrPesquisar.setVisible(false);
            jbtLimpar.setVisible(false);
            jbtActualizar.setVisible(false);
            jbtEditar.setVisible(true);
            jbtRemover.setVisible(true);
            jbtVoltar.setVisible(true);
            jtfNome.setEnabled(false);
            jtfCodigo.setEnabled(false);
            jcbSala.setEnabled(false);
            jcbCurso.setEnabled(false);
            jcbRegime.setEnabled(false);

        } else if (step.equalsIgnoreCase(STEP_EDITAR)) {
            jbtGravar.setVisible(false);
            jbrPesquisar.setVisible(false);
            jbtLimpar.setVisible(true);
            jbtActualizar.setVisible(true);
            jbtEditar.setVisible(false);
            jbtRemover.setVisible(false);
            
            jtfNome.setEnabled(true);
            jtfCodigo.setEnabled(false);
            jcbSala.setEnabled(true);
            jcbCurso.setEnabled(true);
            jcbRegime.setEnabled(true);
        }
    }

    private void ganharFocus(JTextField tf, String s) {
        if (tf.getText().equals(s)) {
            tf.setText("");
        }
    }

    private void perderFoco(JTextField tf, String s) {
        if (tf.getText().equals("")) {
            tf.setText(s);
        }
    }

    private void jtextFieldSemFoco() {
        jtfCodigo.setText("ex: 20173545");
        jtfNome.setText("ex: Edmilson Cuamba");

    }

  
