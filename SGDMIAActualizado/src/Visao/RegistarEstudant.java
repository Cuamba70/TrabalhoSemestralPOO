    /**
     * butao responsavel por gravar os dados na base dados.
     * @param evt 
     */
    private void jbtGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtGravarActionPerformed

        // TODO add your handling code here:
        EstudanteDAO dau = new EstudanteDAO();
        Estudante b = new Estudante();

        EstudanteDAO estu = new EstudanteDAO();
        int contador = 0;
        for (Estudante a : dau.consular()) {
            if (a.getCodigo() == Integer.parseInt(jtfCodigo.getText())) {
                contador++;
            }
        }
        if (contador == 0) {
            b.setNome(jtfNome.getText());
            b.setCodigo(Integer.parseInt((jtfCodigo.getText())));
            Salas salas = (Salas) jcbSala.getSelectedItem();
            b.setSala(salas);
            b.setCurso((String) jcbCurso.getSelectedItem());
            b.setRegime((String) jcbRegime.getSelectedItem());
            b.setEstado(Constantes.ESTADO_REGISTO_VIGOR);
            b.setData(new Date());

            estu.gravar(b);
            JOptionPane.showMessageDialog(null, "O estudante foi registado com sucesso");
            jtfNome.setText(null);
            jtfCodigo.setText(null);
            jcbCurso.setSelectedIndex(0);
            jcbRegime.setSelectedIndex(0);
            jcbSala.setSelectedIndex(0);
            jtextFieldSemFoco();
            readJTable();
        } else {
            JOptionPane.showMessageDialog(null, "O Codigo introduzido ja foi registado.");
        }
        

    }//GEN-LAST:event_jbtGravarActionPerformed

