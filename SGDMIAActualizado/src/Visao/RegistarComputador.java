    private void jbtAvariadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtAvariadoActionPerformed
        // TODO add your handling code here:
         if (tabela.getSelectedRow() != -1) {
            Computador a = new Computador();
            ComputadorDAO dau = new ComputadorDAO();

            int b = JOptionPane.showConfirmDialog(null, "Com certeza que o computador esta avariado?");
            if (b == 0) {

                a.setDescricao((String) tabela.getValueAt(tabela.getSelectedRow(),1));
                a.setCodigo((String) tabela.getValueAt(tabela.getSelectedRow(), 0));
                a.setComponentes((String) tabela.getValueAt(tabela.getSelectedRow(), 2));
                a.setEstado(Constantes.ESTADO_REGISTO_AVARIADO);
                a.setData((Date) tabela.getValueAt(tabela.getSelectedRow(), 3));
               
                dau.atualizar(a);

                JOptionPane.showMessageDialog(null,
                        "O computador esta avariado.");
                jtfDescricao.setText(null);
                jtfCodigo.setText(null);
                jlistComponentes.setSelectedIndex(0);
                jtextFieldSemFoco();
                readJTable();
                setStep(STEP_CRIAR);
               
            }
         }                   
    }//GEN-LAST:event_jbtAvariadoActionPerformed

    /**
     * Metodo responsavel por buscar os dados na base de dados e mostrar na tabela.
     */
    public void readJTable() {
        DefaultTableModel modelo1 = (DefaultTableModel) tabela.getModel();
        modelo1.setNumRows(0);
        ComputadorDAO dau = new ComputadorDAO();

        for (Computador a : dau.consular()) {
            if ((a.getEstado().equalsIgnoreCase(Constantes.ESTADO_REGISTO_VIGOR))) {
                modelo1.addRow(new Object[]{a.getCodigo(), a.getDescricao(),
                    a.getComponentes(), a.getData()

                });
            }

        }
    }

    /**
     * Metodo responsavel buscar os dados pesquisados e mostrar na tabela.
     */
    public void readJTable1() {
        DefaultTableModel modelo1 = (DefaultTableModel) tabela.getModel();
        modelo1.setNumRows(0);
        ComputadorDAO dau = new ComputadorDAO();

        for (Computador a : dau.consular()) {
            if (a.getCodigo().equalsIgnoreCase(jtfCodigo.getText())) {

                modelo1.addRow(new Object[]{a.getCodigo(), a.getDescricao(),
                    a.getComponentes(), a.getData(), a.getEstado()

                });
            }
        }
    }

    /**
     * metodo que ajuda a desabilitar alguns campos e butoes na tela.
     * @param step 
     */
    public void setStep(String step) {

        if (step.equalsIgnoreCase(STEP_CRIAR)) {
            jbtGravar.setVisible(true);
            jbrPesquisar.setVisible(true);
            jbtLimpar.setVisible(true);
            jbtActualizar.setVisible(false);
            jbtEditar.setVisible(false);
            jbtRemover.setVisible(false);
            jbtAvariado.setVisible(false);
            jbtVoltar.setVisible(false);
            jtfDescricao.setEnabled(true);
            jtfCodigo.setEnabled(true);
            jlistComponentes.setEnabled(true);

        } else if (step.equalsIgnoreCase(STEP_VISUALIZAR)) {
            jbtGravar.setVisible(false);
            jbrPesquisar.setVisible(false);
            jbtLimpar.setVisible(false);
            jbtActualizar.setVisible(false);
            jbtEditar.setVisible(true);
            jbtRemover.setVisible(true);
            jbtAvariado.setVisible(true);
            jbtVoltar.setVisible(true);
            jtfDescricao.setEnabled(false);
            jtfCodigo.setEnabled(false);
            jlistComponentes.setEnabled(false);

        } else if (step.equalsIgnoreCase(STEP_EDITAR)) {
            jbtGravar.setVisible(false);
            jbrPesquisar.setVisible(false);
            jbtLimpar.setVisible(true);
            jbtActualizar.setVisible(true);
            jbtEditar.setVisible(false);
            jbtRemover.setVisible(false);
            jbtAvariado.setVisible(false);
            jbtVoltar.setVisible(true);
            jtfDescricao.setEnabled(true);
            jtfCodigo.setEnabled(false);
            jlistComponentes.setEnabled(true);
        }
    }

    private void ganharFocus(JTextField tf, String s) {
        if (tf.getText().equals(s)) {
            tf.setText("");
        }
    }

    private void perderFoco(JTextField tf, String s) {
        if (tf.getText().equals("")) {
            tf.setText(s);
        }
    }

    private void jtextFieldSemFoco() {
        jtfCodigo.setText("ex: 123M");
        jtfDescricao.setText("ex: ");

    }


