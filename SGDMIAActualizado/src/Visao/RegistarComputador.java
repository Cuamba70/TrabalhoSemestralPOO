    /**
     * butao responsavel por gravar os dados na base dados.
     * @param evt 
     */
    private void jbtGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtGravarActionPerformed
        // TODO add your handling code here:
        ArrayList t = new ArrayList();
        Computador b = new Computador();
        ComputadorDAO estu = new ComputadorDAO();

        int[] selectedIx = this.jlistComponentes.getSelectedIndices();

        for (int i = 0; i < selectedIx.length; i++) {
            t.add(jlistComponentes.getModel().getElementAt(selectedIx[i]));

        }
          int contador = 0;
          
        for (Computador a : estu.consular()) {
            if (a.getCodigo().equalsIgnoreCase(jtfCodigo.getText())) {
                contador++;
            }
        }
        if (contador == 0) {
            b.setDescricao(jtfDescricao.getText());
            b.setCodigo(jtfCodigo.getText());
            b.setComponentes(t.toString());
            b.setEstado(Constantes.ESTADO_REGISTO_VIGOR);
            b.setData(new Date());

            estu.gravar(b);
            JOptionPane.showMessageDialog(null, "O computador foi registado com sucesso");
            jtfDescricao.setText(null);
            jtfCodigo.setText(null);
            jlistComponentes.setSelectedIndex(0);
            jtextFieldSemFoco();
        } else {
            JOptionPane.showMessageDialog(null, "O Codigo introduzido ja foi registado.");
        }

        readJTable();
    }//GEN-LAST:event_jbtGravarActionPerformed

    /**
     * butao responsavel por actualizar dados que ja foram gravados na base de dados.
     * @param evt 
     */
    private void jbtActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtActualizarActionPerformed
        // TODO add your handling code here:
        if (tabela.getSelectedRow() != -1) {
            Computador b = new Computador();
            ComputadorDAO estu = new ComputadorDAO();
            ArrayList t = new ArrayList();

            int[] selectedIx = this.jlistComponentes.getSelectedIndices();

            for (int i = 0; i < selectedIx.length; i++) {
                t.add(jlistComponentes.getModel().getElementAt(selectedIx[i]));

            }
            b.setDescricao(jtfDescricao.getText());
            b.setCodigo((String) tabela.getValueAt(tabela.getSelectedRow(), 0));
            b.setComponentes(t.toString());
            b.setData((Date) tabela.getValueAt(tabela.getSelectedRow(), 3));
            b.setEstado((String) tabela.getValueAt(tabela.getSelectedRow(), 4));
            if (estu.atualizar(b)) {
                JOptionPane.showMessageDialog(null, "O computador foi actualizado com sucesso");
                jtfDescricao.setText(null);
                jtfCodigo.setText(null);
                jlistComponentes.setSelectedIndex(0);
                jtextFieldSemFoco();
                readJTable();
                setStep(STEP_CRIAR);

            } else {
                JOptionPane.showMessageDialog(null, "O Codigo introduzido ja foi registado!!!");
            }

        }
    }//GEN-LAST:event_jbtActualizarActionPerformed

    /**
     * butao responsavel por remover o objecto na base de dados de uma forma logica.
     * Na remocao muda o estudo do registo para removido
     * @param evt 
     */
    private void jbtRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtRemoverActionPerformed
        // TODO add your handling code here:
        if (tabela.getSelectedRow() != -1) {
            Computador a = new Computador();
            ComputadorDAO dau = new ComputadorDAO();
            ArrayList t = new ArrayList();

            int[] selectedIx = this.jlistComponentes.getSelectedIndices();

            for (int i = 0; i < selectedIx.length; i++) {
                t.add(jlistComponentes.getModel().getElementAt(selectedIx[i]));

            }

            int b = JOptionPane.showConfirmDialog(null, "Deseja mesmo remover?");
            if (b == 0) {

                a.setDescricao((String) tabela.getValueAt(tabela.getSelectedRow(),1));
                a.setCodigo((String) tabela.getValueAt(tabela.getSelectedRow(), 0));
                a.setComponentes((String) tabela.getValueAt(tabela.getSelectedRow(), 2));
                a.setEstado(Constantes.ESTADO_REGISTO_REMOVIDO);
                a.setData((Date) tabela.getValueAt(tabela.getSelectedRow(), 3));
               
                dau.atualizar(a);

                JOptionPane.showMessageDialog(null,
                        "Removido com sucesso");
                jtfDescricao.setText(null);
                jtfCodigo.setText(null);
                jlistComponentes.setSelectedIndex(0);
                jtextFieldSemFoco();
                readJTable();
                setStep(STEP_CRIAR);
               
            }

        } else {
            JOptionPane.showMessageDialog(null,
                    "Selecione um computador para poder remover");
        }

    }//GEN-LAST:event_jbtRemoverActionPerformed

