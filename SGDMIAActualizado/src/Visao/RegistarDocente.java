    /**
     * butao responsavel por remover o objecto ba base de dados de uma forma logica.
     * Na remocao muda o estudo do registo para removido 
     * @param evt 
     */
    private void jbtRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtRemoverActionPerformed
        // TODO add your handling code here:
        if (tabela.getSelectedRow() != -1) {
            Docente a = new Docente();
            DocenteDAO dau = new DocenteDAO();

            int b = JOptionPane.showConfirmDialog(null, "Deseja mesmo remover?");
            if (b == 0) {
                a.setNome((String) tabela.getValueAt(tabela.getSelectedRow(), 1));
                a.setCodigo((Integer) tabela.getValueAt(tabela.getSelectedRow(), 0));
                a.setSala((Salas) tabela.getValueAt(tabela.getSelectedRow(), 2));
                a.setEstado(Constantes.ESTADO_REGISTO_REMOVIDO);
                a.setData((Date) tabela.getValueAt(tabela.getSelectedRow(), 3));

                dau.atualizar(a);
                JOptionPane.showMessageDialog(null,
                    "Removido com sucesso");
                jtfNome.setText(null);
                jtfCodigo.setText(null);
                jcbSala.setSelectedIndex(0);
                jtextFieldSemFoco();
                readJTable();
                setStep(STEP_CRIAR);

            } else {
                JOptionPane.showMessageDialog(null,
                    "Selecione um docente para poder remover");
            }
        }
    }//GEN-LAST:event_jbtRemoverActionPerformed

