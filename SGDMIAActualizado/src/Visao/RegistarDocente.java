    /**
     * butao responsavel por actualizar dados que ja foram gravados na base de dados.
     * @param evt 
     */
    private void jbtActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtActualizarActionPerformed
        // TODO add your handling code here:
        if (tabela.getSelectedRow() != -1) {
            Docente b = new Docente();

            DocenteDAO estu = new DocenteDAO();

            b.setNome(jtfNome.getText());
            b.setCodigo(Integer.parseInt(tabela.getValueAt(tabela.getSelectedRow(), 0).toString()));
            b.setSala((Salas) jcbSala.getSelectedItem());
            b.setData((Date) tabela.getValueAt(tabela.getSelectedRow(), 3));
            b.setEstado((String) tabela.getValueAt(tabela.getSelectedRow(), 4));
            if (estu.atualizar(b)) {
                JOptionPane.showMessageDialog(null, "O docente foi actualizado com sucesso");
                jtfNome.setText(null);
                jtfCodigo.setText(null);
                jcbSala.setSelectedIndex(0);
                jtextFieldSemFoco();
                readJTable();
                setStep(STEP_CRIAR);
            } else {
                JOptionPane.showMessageDialog(null, "Nao foi possivel actualizar o docente!!!");
            }

        }
    }//GEN-LAST:event_jbtActualizarActionPerformed

