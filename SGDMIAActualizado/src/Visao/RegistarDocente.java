    /**
     * butao responsavel por gravar os dados na base dados.
     * @param evt 
     */
    private void jbtGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtGravarActionPerformed
        // TODO add your handling code here:
        Docente b = new Docente();

        DocenteDAO estu = new DocenteDAO();
        int contador = 0;
        for (Docente a : estu.consular()) {
            if (a.getCodigo() == Integer.parseInt(jtfCodigo.getText())) {
                contador++;
            }
        }
        if (contador == 0) {
            b.setNome(jtfNome.getText());
            b.setCodigo(Integer.parseInt((jtfCodigo.getText())));
            Salas saladocente = (Salas) jcbSala.getSelectedItem();
            b.setSala(saladocente);
            b.setEstado(Constantes.ESTADO_REGISTO_VIGOR);
            b.setData(new Date());

            estu.gravar(b);

            JOptionPane.showMessageDialog(null, "O docente foi registado com sucesso");
            jtfNome.setText(null);
            jtfCodigo.setText(null);
            jcbSala.setSelectedIndex(0);
            jtextFieldSemFoco();
            readJTable();
        } else {
            JOptionPane.showMessageDialog(null, "O Codigo introduzido ja foi registado.");
        }

    }//GEN-LAST:event_jbtGravarActionPerformed

