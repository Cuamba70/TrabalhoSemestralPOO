    /**
     * butao responsavel por requisitar um determinado material.
     * @param evt 
     */
    private void jbtRequisitarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtRequisitarActionPerformed
        // TODO add your handling code here:
        RequisitarMaterial b = new RequisitarMaterial();
        RequisitarMaterialDAO estu = new RequisitarMaterialDAO();
        ArrayList t = new ArrayList();

        int[] selectedIx = this.jlistMaterialRequistado.getSelectedIndices();

        for (int i = 0; i < selectedIx.length; i++) {
            t.add(jlistMaterialRequistado.getModel().getElementAt(selectedIx[i]));

        }

        Estudante estudante = (Estudante) jcbNomeEstudantes.getSelectedItem();
        b.setNomeestudante(estudante);
        Funcionario funcionario = (Funcionario) jcbNomeFuncionarios.getSelectedItem();
        b.setNomefuncionario(funcionario);
        b.setMaterialrequisitado(t.toString());
        b.setEstado(Constantes.ESTADO_REGISTO_EMPRESTADO);
        b.setData(new Date());

        if (estu.gravar(b)) {
            JOptionPane.showMessageDialog(null, "O Material foi requisitado com sucesso");
            jcbNomeFuncionarios.setSelectedIndex(0);
            jcbNomeFuncionarios.setSelectedIndex(0);
            jlistMaterialRequistado.setSelectedIndex(0);
            readJTable();
        } else {
            JOptionPane.showMessageDialog(null, "Nao foi possivel requistar o material!!!.");
        }

     
    }//GEN-LAST:event_jbtRequisitarActionPerformed

    private void jbtActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtActualizarActionPerformed
        // TODO add your handling code here:
        if (tabela.getSelectedRow() != -1) {
            RequisitarMaterial b = new RequisitarMaterial();
            RequisitarMaterialDAO estu = new RequisitarMaterialDAO();
            ArrayList t = new ArrayList();

            int[] selectedIx = this.jlistMaterialRequistado.getSelectedIndices();

            for (int i = 0; i < selectedIx.length; i++) {
                t.add(jlistMaterialRequistado.getModel().getElementAt(selectedIx[i]));

            }

            b.setCodigo(Integer.parseInt(tabela.getValueAt(tabela.getSelectedRow(), 0).toString()));
            b.setNomeestudante((Estudante) tabela.getValueAt(tabela.getSelectedRow(), 1));
            b.setNomefuncionario((Funcionario) tabela.getValueAt(tabela.getSelectedRow(), 2));
            b.setMaterialrequisitado(t.toString());
            b.setData((Date) tabela.getValueAt(tabela.getSelectedRow(), 4));
            b.setEstado((String) tabela.getValueAt(tabela.getSelectedRow(), 5));

            if (estu.atualizar(b)) {
                JOptionPane.showMessageDialog(null, "Actualizacao efectuada com sucesso");
                jcbNomeFuncionarios.setSelectedIndex(0);
                jcbNomeFuncionarios.setSelectedIndex(0);
                jlistMaterialRequistado.setSelectedIndex(0);
                readJTable();
                setStep(STEP_CRIAR);

            } else {
                JOptionPane.showMessageDialog(null, "Nao foi possivel actualizar a requisicao!!!");
            }

        }
    }//GEN-LAST:event_jbtActualizarActionPerformed

