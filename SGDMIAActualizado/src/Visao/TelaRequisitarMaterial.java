    public void readJTable() {
        DefaultTableModel modelo1 = (DefaultTableModel) tabela.getModel();
        modelo1.setNumRows(0);
        RequisitarMaterialDAO dau = new RequisitarMaterialDAO();

        for (RequisitarMaterial a : dau.consultar()) {
            if ((a.getEstado().equalsIgnoreCase(Constantes.ESTADO_REGISTO_EMPRESTADO))) {
                modelo1.addRow(new Object[]{a.getCodigo(), a.getNomeestudante(),
                    a.getNomefuncionario(), a.getMaterialrequisitado(), a.getData()

                });
            }
        }
    }

