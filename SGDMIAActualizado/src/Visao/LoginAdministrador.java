    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
       
             FuncionarioDAO dau = new FuncionarioDAO();

            int contador = 0;
            if(evt.getSource()==btnEntrar){
            for (Funcionario a : dau.consular()) {
                if (a.getUsuario().equalsIgnoreCase(txtUsuario.getText()) && (a.getSenha().equalsIgnoreCase(txtSenha.getText()))) {
                    contador++;
                }
            }
            if (contador != 0) {
                setVisible(false);
                TelaPrincipal telaPrinciplal = new TelaPrincipal();
                telaPrinciplal.setVisible(true);
                this.setVisible(false);

            } else {
                Tremer();
                JOptionPane.showMessageDialog(null, "Usuário ou Senha incorrecta", "ACESSO NEGADO", JOptionPane.ERROR_MESSAGE);

            }

        }
    }//GEN-LAST:event_btnEntrarActionPerformed

