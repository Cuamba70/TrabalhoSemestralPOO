    public void  Tremer(){   
        Point p = this.getLocation();
        LoginAdministrador login = this;

       new Thread() {
            @Override
            public void run() {
                try {
                    for (int i = 0; i < 4; i++) {
                        login.setLocation(p.x - 10, p.y);

                        sleep(500);
                        login.setLocation(p.x - 10, p.y);
                        sleep(500);
                        
                    }
                } catch (InterruptedException ex) {
                    Logger.getLogger(LoginAdministrador.class.getName()).log(Level.SEVERE, null, ex);
                }

            }
        } .start();
            
     }

