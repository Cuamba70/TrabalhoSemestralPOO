    /**
     * Metodo responsavel por buscar os dados na base de dados e mostrar na tabela.
     */
    public void readJTable() {
        DefaultTableModel modelo1 = (DefaultTableModel) tabela.getModel();
        modelo1.setNumRows(0);
        RequisitarMaterialDAO dau = new RequisitarMaterialDAO();

        for (RequisitarMaterial a : dau.consultar()) {
            if ((a.getEstado().equalsIgnoreCase(Constantes.ESTADO_REGISTO_EMPRESTADO))) {
                modelo1.addRow(new Object[]{a.getCodigo(), a.getNomeestudante(),
                    a.getNomefuncionario(), a.getMaterialrequisitado(), a.getData()

                });
            }
        }
    }

    /**
     * Metodo responsavel buscar os dados pesquisados e mostrar na tabela.
     */
    public void readJTable1() {
        DefaultTableModel modelo1 = (DefaultTableModel) tabela.getModel();
        modelo1.setNumRows(0);
        RequisitarMaterialDAO dau = new RequisitarMaterialDAO();

        for (RequisitarMaterial a : dau.consultar()) {
            if (a.getNomeestudante().equals(jcbNomeEstudantes.getSelectedItem())) {

                modelo1.addRow(new Object[]{a.getCodigo(), a.getNomeestudante(),
                    a.getNomefuncionario(), a.getMaterialrequisitado(), a.getData()

                });
            }
        }
    }

    /**
     * metodo que ajuda a desabilitar alguns campos e butoes na tela.
     * @param step 
     */
    public void setStep(String step) {

        if (step.equalsIgnoreCase(STEP_CRIAR)) {
            jbtDevolver.setVisible(true);
            jbrPesquisar.setVisible(true);
            jcbNomeEstudantes.setEnabled(true);
            jcbNomeFuncionarios.setEnabled(true);
            jlistMaterialRequistado.setEnabled(false);

        } else if (step.equalsIgnoreCase(STEP_VISUALIZAR)) {
            jbtDevolver.setVisible(true);
            jbrPesquisar.setVisible(true);
            jcbNomeEstudantes.setEnabled(true);
            jcbNomeFuncionarios.setEnabled(true);
            jlistMaterialRequistado.setEnabled(true);

        } 
    }


