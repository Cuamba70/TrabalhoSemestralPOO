    /**
     * butao responsavel por devolver um determiando material requisitado.
     * @param evt 
     */
    private void jbtDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtDevolverActionPerformed
        // TODO add your handling code here:
          if (tabela.getSelectedRow() != -1) {
            RequisitarMaterial b = new RequisitarMaterial();
            RequisitarMaterialDAO estu = new RequisitarMaterialDAO();
            ArrayList t = new ArrayList();

            int[] selectedIx = this.jlistMaterialRequistado.getSelectedIndices();

            for (int i = 0; i < selectedIx.length; i++) {
                t.add(jlistMaterialRequistado.getModel().getElementAt(selectedIx[i]));

            }

            b.setCodigo(Integer.parseInt(tabela.getValueAt(tabela.getSelectedRow(), 0).toString()));
            b.setNomeestudante((Estudante) tabela.getValueAt(tabela.getSelectedRow(), 1));
            b.setNomefuncionario((Funcionario) tabela.getValueAt(tabela.getSelectedRow(), 2));
            b.setMaterialrequisitado((String) tabela.getValueAt(tabela.getSelectedRow(), 3));
            b.setData((Date) tabela.getValueAt(tabela.getSelectedRow(), 4));
            b.setEstado(Constantes.ESTADO_REGISTO_VIGOR);

            if (estu.atualizar(b)) {
                JOptionPane.showMessageDialog(null, "Material devolvido com sucesso.");
                jcbNomeFuncionarios.setSelectedIndex(0);
                jlistMaterialRequistado.setSelectedIndex(0);
                readJTable();
                setStep(STEP_CRIAR);

            } else {
                JOptionPane.showMessageDialog(null, "Nao foi possivel devolver o material!!!");
            }
            readJTable();

        }

    }//GEN-LAST:event_jbtDevolverActionPerformed

